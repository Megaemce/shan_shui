/**
 * Represents an interface for a range with left and right boundaries.
 *
 * @interface
 */
export default interface IRange {
    /**
     * The beginning boundary of the range.
     *
     * @type {number}
     */
    start: number;

    /**
     * The end boundary of the range.
     *
     * @type {number}
     */
    end: number;
}
