/**
 * Represents an interface for a range with left and right boundaries.
 *
 * @interface
 */
export default interface IRange {
    /**
     * The left boundary of the range.
     *
     * @type {number}
     */
    left: number;

    /**
     * The right boundary of the range.
     *
     * @type {number}
     */
    right: number;
}
