/**
 * Represents an interface for a 2D point with x and y coordinates.
 *
 * @interface
 */
export default interface IPoint {
    /**
     * The x-coordinate of the 2D point.
     *
     * @type {number}
     */
    x: number;

    /**
     * The y-coordinate of the 2D point.
     *
     * @type {number}
     */
    y: number;
}
